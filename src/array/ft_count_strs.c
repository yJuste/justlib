#include "justlib.h"

int	ft_count_strs(const char **s, const char *str)
{
	int			i;
	int			j;

	i = 0;
	j = 0;
	while (s[i])
		if (ft_strcmp(s[i++], str) == 0)
			j++;
	return (j);
}
