#include "justlib.h"

void	*ft_memcpy(void *dst, const void *src, int n)
{
	int		i;

	i = 0;
	if (!dst && !src)
		return (NULL);
	while (i < n)
	{
		((unsigned char *)dst)[i] = ((const unsigned char *)src)[i];
		i++;
	}
	return (dst);
}
